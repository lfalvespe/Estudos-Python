# Biblioteca 'MATH'
import math

a = -2.5
b = 2
c = 30
d = 0.5
e = 25

# .trunc(x) - Pega a parte inteira de um número

print(math.trunc(a))

# .fabs(x) - Retorna o valor absoluto de x

print(math.fabs(a))

# .modf(x) - Retorna (a, b) a = parte fracionária, b = parte inteira

print(math.modf(a))

# .ceil(x) - Retorna x arredondado

print(math.ceil(a))

# .floor - Retorna x arredondado

print(math.floor(a))

# .pow(x,y) - Retorna a potência x elevado a y

print(math.pow(a, b))

# .sqrt(x) - Retorna a raiz quadrada de x

print(math.sqrt(e))


# Funcões trigonométricas:

print(math.sin(c))
print(math.cos(c))
print(math.tan(c))
print(math.asin(d))
print(math.acos(d))
print(math.atan(d))

# .radians(x) - Converte o valor de x de graus para radianos

print(math.radians(c))




